function initialState(): number[] {
  return [
    0x6a09e667, 0xbb67ae85, 0x3c6ef372, 0xa54ff53a,
    0x510e527f, 0x9b05688c, 0x1f83d9ab, 0x5be0cd19,
  ];
}

function binToHexBig(binArray: number[]): string {
  const hexTab = "0123456789abcdef";
  let str = "";
  for (let i = 0; i < binArray.length * 4; i++) {
    const shift = (3 - (i % 4)) * 8;
    str +=
      hexTab.charAt((binArray[i >> 2] >> (shift + 4)) & 0xf) +
      hexTab.charAt((binArray[i >> 2] >> shift) & 0xf);
  }
  return str;
}

function strToBinBig(str: string): number[] {
  const bin: number[] = [];
  const mask = (1 << 8) - 1;
  for (let i = 0; i < str.length * 8; i += 8) {
    const idx = i >> 5;
    const shift = 24 - (i % 32);
    bin[idx] = (bin[idx] || 0) | ((str.charCodeAt(i / 8) & mask) << shift);
  }
  return bin;
}

function core_sha256(x: number[], len: number): number[] {
  // big-endian
  x[len >> 5] = (x[len >> 5] || 0) | (0x80 << (24 - (len % 32)));
  const totalWords = (((len + 64) >>> 9) << 4) + 15;

  x[totalWords - 1] = x[totalWords - 1] || 0;
  x[(((len + 64) >>> 9) << 4) + 14] = Math.floor(len / 0x100000000);
  x[(((len + 64) >>> 9) << 4) + 15] = len & 0xffffffff;

  for (let i = 0; i < x.length; i++) if (x[i] === undefined) x[i] = 0;

  const H = initialState();
  const W = new Array(64);

  for (let i = 0; i < x.length; i += 16) {
    for (let t = 0; t < 16; t++) W[t] = x[i + t] | 0;
    for (let t = 16; t < 64; t++) {
      W[t] = (σ1(W[t - 2]) + W[t - 7] + σ0(W[t - 15]) + W[t - 16]) & 0xffffffff;
    }

    let a = H[0] | 0;
    let b = H[1] | 0;
    let c = H[2] | 0;
    let d = H[3] | 0;
    let e = H[4] | 0;
    let f = H[5] | 0;
    let g = H[6] | 0;
    let h = H[7] | 0;

    for (let t = 0; t < 64; t++) {
      const T1 = (h + Σ1(e) + Ch(e, f, g) + K[t] + W[t]) & 0xffffffff;
      const T2 = (Σ0(a) + Maj(a, b, c)) & 0xffffffff;
      h = g;
      g = f;
      f = e;
      e = (d + T1) & 0xffffffff;
      d = c;
      c = b;
      b = a;
      a = (T1 + T2) & 0xffffffff;
    }

    H[0] = (H[0] + a) & 0xffffffff;
    H[1] = (H[1] + b) & 0xffffffff;
    H[2] = (H[2] + c) & 0xffffffff;
    H[3] = (H[3] + d) & 0xffffffff;
    H[4] = (H[4] + e) & 0xffffffff;
    H[5] = (H[5] + f) & 0xffffffff;
    H[6] = (H[6] + g) & 0xffffffff;
    H[7] = (H[7] + h) & 0xffffffff;
  }

  return H;
}

export function sha256(str: string): string {
  str = utf8Encode(str);
  return binToHexBig(core_sha256(strToBinBig(str), str.length * 8));
}

export function sha256_raw(binaryStr: string): string {
  return binToHexBig(core_sha256(strToBinBig(binaryStr), binaryStr.length * 8));
}
