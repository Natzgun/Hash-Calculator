import { utf8Encode } from './utils';

function rotr(x: number, n: number): number {
  return (x >>> n) | ((x << (32 - n)) & 0xffffffff);
}
function shr(x: number, n: number): number {
  return x >>> n;
}

// SHA-256
function Σ0(x: number): number { return rotr(x, 2) ^ rotr(x, 13) ^ rotr(x, 22); }
function Σ1(x: number): number { return rotr(x, 6) ^ rotr(x, 11) ^ rotr(x, 25); }
function σ0(x: number): number { return rotr(x, 7) ^ rotr(x, 18) ^ shr(x, 3); }
function σ1(x: number): number { return rotr(x, 17) ^ rotr(x, 19) ^ shr(x, 10); }
function Ch(x: number, y: number, z: number): number { return (x & y) ^ (~x & z); }
function Maj(x: number, y: number, z: number): number { return (x & y) ^ (x & z) ^ (y & z); }

